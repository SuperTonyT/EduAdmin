package table;

public class S_ScoreTable {
    private String term;
    private String c_number;
    private String c_name;
    private String t_number;
    private String t_name;
    private String time;
    private float assignment_score;
    private float test_score;
    private float score;

    public String getTerm() {
        return term;
    }

    public void setTerm(String term) {
        this.term = term;
    }

    public String getC_number() {
        return c_number;
    }

    public void setC_number(String c_number) {
        this.c_number = c_number;
    }

    public String getC_name() {
        return c_name;
    }

    public void setC_name(String c_name) {
        this.c_name = c_name;
    }

    public String getT_number() {
        return t_number;
    }

    public void setT_number(String t_number) {
        this.t_number = t_number;
    }

    public String getT_name() {
        return t_name;
    }

    public void setT_name(String t_name) {
        this.t_name = t_name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public float getAssignment_score() {
        return assignment_score;
    }

    public void setAssignment_score(float assignment_score) {
        this.assignment_score = assignment_score;
    }

    public float getTest_score() {
        return test_score;
    }

    public void setTest_score(float test_score) {
        this.test_score = test_score;
    }

    public float getScore() {
        return score;
    }

    public void setScore(float score) {
        this.score = score;
    }
}
